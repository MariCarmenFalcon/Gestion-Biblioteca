    //AÑADIR USUARIO
    private void buttonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAñadirActionPerformed
        String nombre = txtNombreUsuario.getText();
        String apellidos = txtApellUsuario.getText();
        String dni = txtDni.getText();
        String telefono = txtTelfono.getText();       
        String idUsuario = txtIdUsuarioRegistro.getText();              
     
        t = (DefaultTableModel) tablaUsuarios.getModel();
        String filaUsuario[] = {nombre, apellidos, dni, telefono, idUsuario};     
        t.addRow(filaUsuario);        
        
        try {
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tabla", "root", "");
            System.out.println("Conectado");
            
            //Creo la sentencia SQL de insertar
            String insertQuery = "INSERT INTO usuario (Nombre, Apellidos, Dni, Teléfono, IDUsuario) VALUES (?, ?, ?, ?, ?)";
            
            //Preparo la sentencia
            PreparedStatement preparedStatement = con.prepareStatement(insertQuery);
            preparedStatement.setString(1, nombre);
            preparedStatement.setString(2, apellidos);            
            preparedStatement.setString(3, dni);
            preparedStatement.setString(4, telefono);            
            preparedStatement.setString(5, idUsuario);
            
            //Ejecuto la insercion
            int rowCount = preparedStatement.executeUpdate();
            
            //Cierra la conexion
            preparedStatement.close();
            con.close();
            
        } catch (Exception e) {
            System.out.println("No conectado. Error");
            e.printStackTrace();            
        }
        
        t.fireTableDataChanged();
        actualizarOrden();
    }//GEN-LAST:event_buttonAñadirActionPerformed

    //ELIMINAR USUARIO
    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed

        int fila = tablaUsuarios.getSelectedRow();        

        if (fila >= 0) {            
            String nombre = (String)t.getValueAt(fila, 0);
            t.removeRow(fila);
            
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tabla", "root", "");
                System.out.println("Conectado");
                
                //Creo sentencia de eliminacion SQL
                String deleteQuery = "DELETE FROM usuario WHERE Nombre = ?";                
                
                //Preparo la sentencia
                PreparedStatement preparedStatement = con.prepareStatement(deleteQuery);
                preparedStatement.setString(1, nombre);
                
                //Ejecuto la eliminación
                int rowCount = preparedStatement.executeUpdate();
                
                //cierro la conexion
                preparedStatement.close();
                con.close();                
                
            } catch (Exception e) {
                System.out.println("No conectado. Error");
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona una fila");
        }        
        
        t.fireTableDataChanged();
        actualizarOrden();
    }//GEN-LAST:event_buttonEliminarActionPerformed

