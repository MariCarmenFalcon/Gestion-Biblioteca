    public void ConectarBaseDatos(){
        
         PrepararTabla();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tabla", "root", "");
            System.out.println("Conectado");
            
            String query = "SELECT * FROM usuario";
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            
            t.setRowCount(0);
            
            while (rs.next()) {                
                String nombre = rs.getString("Nombre");
                String apellidos = rs.getString("Apellidos");                
                String dni = rs.getString("Dni");
                String telefono = rs.getString("Teléfono");                
                String idUsuario = rs.getString("IDUsuario");
                
                t.addRow(new Object[]{nombre, apellidos, dni, telefono, idUsuario});
            }
            rs.close();
            stmt.close();
            con.close();
            
        } catch (Exception ex) {
            System.out.println("No conectado. Error");
            ex.printStackTrace();
        }
        
    }
    void PrepararTabla(){       
        
        String titulos[] = {"Nombre", "Apellidos", "Dni", "Teléfono", "ID Usuario"};
        t = new DefaultTableModel(null, titulos);
        tablaUsuarios.doLayout();
        tablaUsuarios.setModel(t);
        tablaUsuarios.setDefaultEditor(Object.class, null);
        
        // Inicializar el TableRowSorter
        sorter = new TableRowSorter<>(t);
        tablaUsuarios.setRowSorter(sorter);
        
        // Definir el Comparator para la columna ID Usuario
        sorter.setComparator(4, new Comparator<String>() {
            @Override
            public int compare(String s1, String s2) {
                // Aquí se define la lógica de ordenamiento (teniendo en cuenta que son cadenas de texto).
                return Integer.parseInt(s1) - Integer.parseInt(s2);
            }
        });
    }
    
    // Método para actualizar el orden después de editar o añadir
    private void actualizarOrden() {
        sorter.setModel(t);
    }
    
